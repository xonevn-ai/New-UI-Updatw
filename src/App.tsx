const TemplatePreview = ({ previews, isSelected }: { previews: React.ReactNode[], isSelected: boolean }) => {
  const [isHovered, setIsHovered] = useState(false);
  const [currentIndex, setCurrentIndex] = useState(0);

  useEffect(() => {
    let interval: NodeJS.Timeout;
    if (isHovered) {
      interval = setInterval(() => {
        setCurrentIndex((prev) => (prev + 1) % previews.length);
      }, 1200);
    } else {
      setCurrentIndex(0);
    }
    return () => clearInterval(interval);
  }, [isHovered, previews.length]);

  return (
    <div 
      className={`w-full aspect-video rounded-md mb-2 overflow-hidden border relative bg-[#181E30] transition-colors ${isSelected ? 'border-blue-500/50 ring-1 ring-blue-500/30' : 'border-[#2E3A59]'}`}
      onMouseEnter={() => setIsHovered(true)}
      onMouseLeave={() => setIsHovered(false)}
    >
      {previews.map((preview, idx) => (
        <div 
          key={idx} 
          className={`absolute inset-0 transition-opacity duration-500 ${idx === currentIndex ? 'opacity-100 z-10' : 'opacity-0 z-0'}`}
        >
          {preview}
        </div>
      ))}
      {/* Progress indicators */}
      <div className={`absolute bottom-1 left-0 right-0 flex justify-center space-x-1 z-20 transition-opacity duration-300 ${isHovered ? 'opacity-100' : 'opacity-0'}`}>
        {previews.map((_, idx) => (
          <div key={idx} className={`h-0.5 w-1.5 rounded-full ${idx === currentIndex ? 'bg-white' : 'bg-white/40'}`} />
        ))}
      </div>
    </div>
  );
};

